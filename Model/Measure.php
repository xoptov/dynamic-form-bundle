<?php

namespace Xoptov\DynamicFormBundle\Model;

abstract class Measure implements MeasureInterface
{
    /** @var mixed */
    protected $id;
    
    /** @var string */
    protected $name;
    
    /** @var string */
    protected $label;

    /**
     * {@inheritdoc}
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * {@inheritdoc}
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * {@inheritdoc}
     */
    public function setLabel($label)
    {
        $this->label = $label;

        return $this;
    }

    /**
     * {@inheritdoc}
     */
    public function getLabel()
    {
        return $this->label;
    }
}